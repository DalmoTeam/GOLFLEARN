package com.golflearn.dto;

public class Location {
	private int locNo;
	private String locSido;
	private String locSigungu;
	
	public int getLocNo() {
		return locNo;
	}
	public void setLocNo(int locNo) {
		this.locNo = locNo;
	}
	public String getLocSido() {
		return locSido;
	}
	public void setLocSido(String locSido) {
		this.locSido = locSido;
	}
	public String getLocSigungu() {
		return locSigungu;
	}
	public void setLocSigungu(String locSigungu) {
		this.locSigungu = locSigungu;
	}
}
